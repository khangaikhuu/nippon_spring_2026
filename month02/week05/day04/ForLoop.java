
public class ForLoop {

    public static void main(String[] args) {
        for (int i = 3; i <= 5; i++) {
            System.out.println(i + " ");
        }

        // convert while loop into for loop
        for (int i = 2; i < 9; i = i + 2) {
            System.out.print(i + " ");
        }
        System.out.println();

    }
}
