public class InputExample {
    // method or function
    public static void main(String[] args) {
        /**
         * Comment
         */
        // one line comment
        // Data types
        // 1. Java Integer буюу бүхэл тоон утга
        // RAM - Memory - 32 бит - 2s complement
        // From -2,147,483,648 to 2,147,483,647.
        int buhelToo = 12345; // statement (semicolon) ;
        System.out.println(buhelToo);

        // 2. Java Double буюу бутархай тоон утга
        // RAM - Memory - 64bit 
        double butarhaiToo = 3.14;
        System.out.println(butarhaiToo);

        // 3. Java Boolean буюу Логик утга
        boolean isStudent = true;
        System.out.println(isStudent);

        // 4. Character буюу үсэг утга
        char grade = 'A';
        System.out.println(grade);

        // 5. String буюу үгэн утга
        String name = "Eegii";
        System.out.println(name);
    }
}
